<?php
//예제 5-4 오름차순으로 버블 정렬하기
  $num = array(15, 13, 9, 7, 6, 12, 19, 30, 28, 26);
  $count = 10; // 배열 원소의 개수

  echo "정렬 전 : ";
  for($i=0;$i<$count;$i++){
    echo $num[$i]." ";
  }
  echo "<br>";

  for($i = $count - 2; $i>=0; $i--){
    for($j = 0; $j <= $i; $j++){
      if($num[$j]>$num[$j+1]){
        $tmp = $num[$j];
        $num[$j] = $num[$j+1];
        $num[$j+1] = $tmp;
      }
    }
  }

  echo "버블 정렬(오름차순) 후 : ";
  for($i=0;$i<$count;$i++){
    echo $num[$i]." ";
  }

  //정렬 전 : 15 13 9 7 6 12 19 30 28 26 <br>버블 정렬(오름차순) 후 : 6 7 9 12 13 15 19 26 28 30

  echo "<br><br>";
?>

